const Joi = require("joi");
Joi.objectId = require("joi-objectid")(Joi);

const userSignUpSchemaValidator = Joi.object({
  email: Joi.string()
    .required()
    .email()
    .regex(
      /(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9]))\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/
    ),
  name: Joi.string().required(),
  age: Joi.number().required().greater(0),
  role: Joi.string().required(),
  password: Joi.string(),
  subscriptionId: Joi.string()
});

const userLoginSchemaValidator = Joi.object({
  email: Joi.string()
    .email()
    .regex(
      /(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9]))\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/
    )
    .required(),
  password: Joi.string().required(),
});

const updateUserSchemaValidator = Joi.object({
  userId: Joi.objectId().required(),
  subscription: Joi.objectId(),
  name: Joi.string(),
  age: Joi.number().greater(0).positive(),
  password: Joi.string(),
}).unknown();


const searchUserValidationSchema = Joi.object({
  id : Joi.objectId().required()
})

module.exports = {
  userSignUpSchemaValidator,
  userLoginSchemaValidator,
  updateUserSchemaValidator,
  searchUserValidationSchema
};
